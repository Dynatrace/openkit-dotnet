using System.IO;
using System.IO.Compression;

namespace Dynatrace.OpenKit.Util
{
    /// <summary>
    /// Extension class used in a test, since CopyTo is not available in .NET 3.5
    /// </summary>
    public static class GZipStreamExtensions
    {
        public static long CopyTo(this GZipStream source, Stream destination)
        {
            byte[] buffer = new byte[2048];
            int bytesRead;
            long totalBytes = 0;
            while ((bytesRead = source.Read(buffer, 0, buffer.Length)) > 0)
            {
                destination.Write(buffer, 0, bytesRead);
                totalBytes += bytesRead;
            }
            return totalBytes;
        }
    }
}
